-- カテゴリの階層制限トリガーの作成
create trigger trg_categories_depth before
   insert or update on categories
   for each row
   when ( new.parent_category_id is not null )
declare
   v_parent_parent_id categories.parent_category_id%type;
begin
   select parent_category_id
     into v_parent_parent_id
     from categories
    where category_id = :new.parent_category_id;

   if v_parent_parent_id is not null then
      raise_application_error(
         -20001,
         'カテゴリは2階層までしか許可されていません'
      );
   end if;
end;
